<?php

namespace luckyBundle\Entity;

/**
 * Video
 */
class Video
{
    /**
     * @var string
     */
    private $url;

    /**
     * @var integer
     */
    private $id;


    /**
     * Set url
     *
     * @param string $url
     *
     * @return Video
     */
    public function setUrl($url)
    {
        $this->url = $url;

        return $this;
    }

    /**
     * Get url
     *
     * @return string
     */
    public function getUrl()
    {
        return $this->url;
    }

    /**
     * Get id
     *
     * @return integer
     */
    public function getId()
    {
        return $this->id;
    }
    /**
     * @var \luckyBundle\Entity\Song
     */
    private $song;


    /**
     * Set song
     *
     * @param \luckyBundle\Entity\Song $song
     *
     * @return Video
     */
    public function setSong(\luckyBundle\Entity\Song $song = null)
    {
        $this->song = $song;

        return $this;
    }

    /**
     * Get song
     *
     * @return \luckyBundle\Entity\Song
     */
    public function getSong()
    {
        return $this->song;
    }
}
